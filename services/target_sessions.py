def _build_target_sessions():
    targets = {}
    blocks = (EXTRA_TARGETS,) if EXTRA_TARGETS.strip() else (RAW_TARGETS, EXTRA_TARGETS)
    for block in blocks:
        for line in block.splitlines():
            line = line.strip()
            if not line:
                continue
            batch_label, session_id = line.split("\t", 1)
            batch_number = int(re.search(r"\d+", batch_label).group())
            targets.setdefault(batch_number, set()).add(session_id.strip().rstrip(","))
    return targets


TARGET_SESSIONS_BY_BATCH = _build_target_sessions()


def get_target_sessions_for_sheet(sheet_name: str):
    match = re.search(r"\d+", str(sheet_name or ""))
    if not match:
        return None
    return TARGET_SESSIONS_BY_BATCH.get(int(match.group()))


